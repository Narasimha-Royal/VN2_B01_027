print("--------Strings-------")
a = 'Narasimha'
b = 'Royal'
print("Given String is :", a)
print("---------Addition of Strings---------")
print("Addition of Two Strings :", a + b)
print("-------Strings Multiplication--------")
print("String Multiplication :", b * 3)
print("Max of String :", max(a))
print("Min of String :", min(a))
print("-------Strings with For Loops-------")
for i in a:
    print(i, end=", ")
print()
print("--------Strings with Slicing---------")
for i in a[4:]:
    print(i, end=",")
print()
